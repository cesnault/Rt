#include "ft_ori.h"

void	ft_putnbr_ui(unsigned int nb)
{
	if (nb > 9)
	{
		ft_putnbr_ui(nb / 10);
		ft_putnbr_ui(nb % 10);
	}
	else
	{
		ft_putchar(nb + '0');
		nb = 0;
	}
}
