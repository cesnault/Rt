#include "ft_1d_int.h"
#include <stdlib.h>

void	ft_1d_int_del(t_1d_int **tab_1d)
{
	if (!tab_1d || !(*tab_1d))
		return ;
	ft_1d_int_del_tab(tab_1d);
	free(*tab_1d);
	*tab_1d = 0;
}
