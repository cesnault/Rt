#include "rgb.h"

t_rgb	rgb_add(t_rgb v1, t_rgb v2)
{
	t_rgb r;

	r.a = v1.a + v2.a;
	r.g = v1.g + v2.g;
	r.r = v1.r + v2.r;
	r.b = v1.b + v2.b;
	if (r.b > 255)
		r.b = 255;
	if (r.a > 255)
		r.a = 255;
	if (r.g > 255)
		r.g = 255;
	if (r.r > 255)
		r.r = 255;
	return (r);
}
